a = 8
b = 4

print("Assign Values:\t\t", "a=", a, "\tb=", b)

a+=b
print("Add & Assign:\t\t", "a=", a, "(8+4)")

a-=b
print("Subtract & Assign:\t", "a=", a, "(12-4)")

a*=b
print("Multiple & Assign:\t", "a=", a, "(8*4)")

a/=b
print("Divide & Assign:\t", "a=", a, "(32/4)")

a//=b
print("Floor Divide & Assign:\t", "a=", a, "(8//4)")

a%=b
print("Mod & Assign:\t\t", "a=", a, "(1%4)")

a**=b
print("Exponent & Assign:\t", "a=", a, "(2^4)")